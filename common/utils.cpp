	bool
		urlify_smth(const unsigned char *str, unsigned char *target, int64_t len)
	{
		int64_t filled = 0;
		char escape[6];
		escape[0] = '%';
		escape[3] = 0;

		const unsigned char *cptr = str;
		if (target)
		{
			for (; *cptr; cptr++)
			{
				if ((*cptr >= 'A' && *cptr <= 'Z') ||
					(*cptr >= 'a' && *cptr <= 'z') ||
					(*cptr >= '0' && *cptr <= '9') ||
					*cptr == '-' || *cptr == '_' || *cptr == '.' ||
					*cptr == '!' || *cptr == '~' || *cptr == '*' ||
					/* *cptr == '\''||*/ *cptr == '(' || *cptr == ')' ||
					(*cptr == '%' &&
					((cptr[1] >= 'A' && cptr[1] <= 'F') ||
						(cptr[1] >= 'a' && cptr[1] <= 'f') ||
						(cptr[1] >= '0' && cptr[1] <= '9')) &&
						((cptr[2] >= 'A' && cptr[2] <= 'F') ||
						(cptr[2] >= 'a' && cptr[2] <= 'f') ||
							(cptr[2] >= '0' && cptr[2] <= '9'))))
				{
					if (filled >= len)
						return false;
					*target++ = *cptr;
					filled++;
				}
				else
				{
					if ((*cptr >> 4) < 10)
						escape[1] = '0' + (*cptr >> 4);
					else
						escape[1] = 'A' - 10 + (*cptr >> 4);
					if ((*cptr & 0xF) < 10)
						escape[2] = '0' + (*cptr & 0xF);
					else
						escape[2] = 'A' - 10 + (*cptr & 0xF);

					if (filled >= len - 3)
						return false;
					*target++ = escape[0];
					*target++ = escape[1];
					*target++ = escape[2];
					filled += 3;
				}
			}
		}
		if (filled < len)
			*target++ = 0;
		return true;
	}

